const firstWord = prompt("Skriv in ett ord");
const secondWord = prompt("Skriv in ett ord till"); 

if(firstWord.toLowerCase()>secondWord.toLowerCase()) {
    alert("Ditt första ord kommer efter ditt andra ord i alfabetet")
} 
else if(firstWord.toLowerCase()<secondWord.toLowerCase()); {
    alert("Ditt första ord kommer före ditt andra ord i alfabetet");
}

    
